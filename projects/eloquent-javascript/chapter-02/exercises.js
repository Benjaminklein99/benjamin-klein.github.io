function triangles(num) {
  let char = '#';
  for (let i = 1; i <= num; i++){
    console.log(char.repeat(i));
  }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz(start, finish) {
  for (let i = start; i <= finish; i++){
    if (i % 3 === 0 && i % 5 === 0){
      console.log('fizzbuzz');
    } else if (i % 3 === 0){
      console.log('fizz');
    } else if (i % 5 === 0){
      console.log('buzz');
    } else {
      console.log(i);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  let board = '';
  for (let x = 0; x < size; x++){
    for (let y = 0; y < size; y++){
      if ((x + y) % 2 === 0){
        board += ' ';
      } else {
        board += '#';
      }
    }
    board += '\n';
  }
  console.log(board);
}


