////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(numOne, numTwo) {
  if (numOne > numTwo){
    return numTwo;
  } else if (numTwo > numOne){
    return numOne;
  } else if (numOne === numTwo){
    return numOne;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num === 0){
    return true;
  } else if (num % 2 === 0){
    return true;
  } else {
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  let count = 0;
  for (let i = 0; i < str.length; i++){
    if (str[i].toUpperCase() === char.toUpperCase()){
      count++;
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  let count = 0;
  for (let i = 0; i < str.length; i++){
    if (str[i].toUpperCase() === 'B'){
      count++;
    }
  }
  return count;
}

